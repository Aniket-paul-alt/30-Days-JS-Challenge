let a = 10 , b = 20, num1 = 50, num2 = 50

//Task 8
let compare1 = a>b // 10>20
console.log(compare1); // false

let compare2 = a<b // 10<20
console.log(compare2); // true

//Task 9
let compare3 = num1 >= num2 // 50 >= 50
console.log(compare3); // true

let compare4 = num1 <= b // 50 <= 20
console.log(compare4);

//Task 10
let compare5 = ( 2 == '2' ) // comparing only value (less strict)
console.log(compare5); // true

let compare6 = ( 2 === '2' ) // comparing value and datatype both (strict check)
console.log(compare6); // false

